$policyJson | Out-File -FilePath iam-policy.json -Encoding UTF8

Write-Host "Creating IAM policy..."
aws iam create-policy --policy-name AWSLoadBalancerControllerIAMPolicy --policy-document file://iam-policy.json --region il-central-1

# Step 2: Associate OIDC provider
Write-Host "`nStep 2: Creating OIDC provider..." -ForegroundColor Yellow
eksctl utils associate-iam-oidc-provider --region=il-central-1 --cluster=domforgeai-cluster --approve

# Step 3: Create IAM Service Account
Write-Host "`nStep 3: Creating IAM Service Account..." -ForegroundColor Yellow
eksctl create iamserviceaccount --cluster=domforgeai-cluster --namespace=kube-system --name=aws-load-balancer-controller --role-name AmazonEKSLoadBalancerControllerRole --attach-policy-arn=arn:aws:iam::797525201856:policy/AWSLoadBalancerControllerIAMPolicy --region=il-central-1 --approve

# Step 4: Install cert-manager
Write-Host "`nStep 4: Installing cert-manager..." -ForegroundColor Yellow
kubectl apply --validate=false -f https://github.com/cert-manager/cert-manager/releases/download/v1.13.0/cert-manager.yaml

Write-Host "Waiting for cert-manager..."
Start-Sleep -Seconds 30

# Step 5: Install AWS Load Balancer Controller
Write-Host "`nStep 5: Installing AWS Load Balancer Controller..." -ForegroundColor Yellow
kubectl apply -k "github.com/aws/eks-charts/stable/aws-load-balancer-controller//crds?ref=master"

$controllerUrl = "https://github.com/kubernetes-sigs/aws-load-balancer-controller/releases/download/v2.7.0/v2_7_0_full.yaml"
Invoke-WebRequest -Uri $controllerUrl -OutFile controller.yaml

(Get-Content controller.yaml) -replace '--cluster-name=your-cluster-name', '--cluster-name=domforgeai-cluster' | Set-Content controller.yaml

kubectl apply -f controller.yaml

Write-Host "`nVerifying installation..." -ForegroundColor Yellow
Start-Sleep -Seconds 20
kubectl get deployment -n kube-system aws-load-balancer-controller

Write-Host "`nAWS Load Balancer Controller Installation Complete!" -ForegroundColor Green
